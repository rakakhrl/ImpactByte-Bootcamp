let angka = 4;

switch (angka) {
  case 1:
    console.log("Senin");
    break;
  case 2:
    console.log("Selasa");
    break;
  case 3:
    console.log("Rabu");
    break;
  case 4:
    console.log("Kamis");
    break;
  case 5:
    console.log("jumat");
    break;
  case 6:
    console.log("sabtu");
    break;
  case 7:
    console.log("minggu");
    break;
  default:
    console.log("Angka tidak sesuai");
    break;
}
